// splice changes orignal array

let array1 = [1,3,5,6,7,8]

console.log('splice');
console.log(array1.splice(2));
console.log(array1);

// slice return new changed array, orignal array remains intact
let array2 = [1,3,5,6,7,8]

console.log('slice');
console.log(array2.slice(2));
console.log(array2);
